# Definition of dictionary
europe = {'spain':'madrid', 'france':'paris', 'germany':'berlin', 'norway':'oslo' }

# Add italy to europe
europe["italy"]="rome"

# Print out italy in europe   
print("italy" in europe)       #CHECKS ELEMENT ADDITION

# Add poland to europe
europe["poland"]="warsaw"

# Print europe
print(europe)

